// Load up the discord.js library
const Discord = require("discord.js");
// We also load the rest of the things we need in this file:
const { promisify } = require("util");
const readdir = promisify(require("fs").readdir);
const Enmap = require("enmap");

// This is your client. Some people call it `bot`, some people call it `self`,
// some might call it `cootchie`. Either way, when you see `client.something`,
// or `bot.something`, this is what we're refering to. Your client.
const client = new Discord.Client();

client.wordList = [];
client.emotes = {};
client.variables = require('./lol/variables.json');
client.lolUsers = require('./lol/users.json');
client.itemsJSON = require('./lol/items.json');
client.items = [];

client.itemsJSON.forEach(item => {
  client.items[item.id] = item.name;
});

client.uuids = {};

const http = require('http');

http.get('http://64.225.96.222:8080/players', (res) => {
  const { statusCode } = res;
  const contentType = res.headers['content-type'];

  let error;
  if (statusCode !== 200) {
    error = new Error('Request Failed.\n' +
                      `Status Code: ${statusCode}`);
  }
  if (error) {
    console.error(error.message);
    // Consume response data to free up memory
    res.resume();
    return;
  }

  res.setEncoding('utf8');
  let rawData = '';
  res.on('data', (chunk) => { rawData += chunk; });
  res.on('end', () => {
    try {
      const parsedData = JSON.parse(rawData);

      Object.keys(parsedData).forEach(k => {
        client.uuids[parsedData[k].toLowerCase()] = k;
      });
    } catch (e) {
      console.error(e.message);
    }
  });
}).on('error', (e) => {
  console.error(`Got error: ${e.message}`);
});

const fs = require("fs");

fs.readdir("./img/", (err, files) => {
  files.forEach(file => {
    const name = file.slice(0, file.lastIndexOf(".")).toLowerCase();
    client.wordList.push(name);
    client.emotes[name] = "./img/"+file;
  });
});

